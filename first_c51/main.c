/*********************************************************************************************
说明：本程序可使单片机P1.0接口上的LED灯以1秒为周期闪烁。

/*********************************************************************************************/

#include <REG51.h> //通用89C51头文件

sbit LED = P1 ^ 0; //定义闪烁LED

/*********************************************************************************************
函数名：毫秒级CPU延时函数
调  用：DELAY_MS (?);
参  数：1~65535（参数不可为0）
返回值：无
结  果：占用CPU方式延时与参数数值相同的毫秒时间
备  注：应用于1T单片机时i<600，应用于12T单片机时i<125											  
/*********************************************************************************************/
void DELAY_MS (unsigned int a){
	unsigned int i;
	while( a-- != 0){
		for(i = 0; i < 600; i++);
	}
}
/*********************************************************************************************/

/*********************************************************************************************/
void main (void){ //主程序 
	while(1){
		LED = ~LED;		//取LED相反状态
		DELAY_MS(100);	//修改这里的数值看看会有什么变化
	}
}
